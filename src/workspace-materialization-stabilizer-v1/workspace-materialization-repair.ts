/**
 * Workspace Materialization Stabilizer V1 — repair.
 *
 * Applies small, targeted, evidence-driven repairs for REPAIRABLE findings only. BLOCKING
 * findings (e.g. missing package.json) are never repaired — no fake repairs, no blind
 * regeneration of the whole application, no touching unrelated files.
 */

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import type {
  WorkspaceFeatureModuleEvidence,
  WorkspaceMaterializationFinding,
  WorkspaceMaterializationRepairAction,
} from './workspace-materialization-types.js';
import { fileExists, findBrokenImportsInFile, findFuzzyImportMatch, pascalCase, titleCase } from './workspace-materialization-validator.js';

function writeGenerated(workspaceDir: string, relPath: string, content: string): void {
  const absPath = join(workspaceDir, relPath);
  mkdirSync(dirname(absPath), { recursive: true });
  writeFileSync(absPath, content, 'utf8');
}

function minimalTsconfig(): string {
  return `${JSON.stringify(
    {
      compilerOptions: {
        target: 'ES2020',
        useDefineForClassFields: true,
        lib: ['ES2020', 'DOM', 'DOM.Iterable'],
        module: 'ESNext',
        skipLibCheck: true,
        moduleResolution: 'bundler',
        allowImportingTsExtensions: true,
        isolatedModules: true,
        moduleDetection: 'force',
        noEmit: true,
        jsx: 'react-jsx',
        strict: true,
      },
      include: ['src'],
    },
    null,
    2,
  )}\n`;
}

function minimalViteConfig(): string {
  return [
    "import { defineConfig } from 'vite';",
    "import react from '@vitejs/plugin-react';",
    '',
    'export default defineConfig({',
    '  plugins: [react()],',
    "  server: { host: '127.0.0.1', port: 5173, strictPort: false },",
    '});',
    '',
  ].join('\n');
}

function minimalIndexHtml(): string {
  return [
    '<!DOCTYPE html>',
    '<html lang="en">',
    '  <head>',
    '    <meta charset="UTF-8" />',
    '    <meta name="viewport" content="width=device-width, initial-scale=1.0" />',
    '    <title>Generated App</title>',
    '  </head>',
    '  <body>',
    '    <div id="root"></div>',
    '    <script type="module" src="/src/main.tsx"></script>',
    '  </body>',
    '</html>',
    '',
  ].join('\n');
}

function minimalMainEntry(): string {
  return [
    "import { StrictMode } from 'react';",
    "import { createRoot } from 'react-dom/client';",
    "import App from './App';",
    '',
    "const rootElement = document.getElementById('root');",
    "if (!rootElement) throw new Error('Root element #root not found');",
    '',
    'createRoot(rootElement).render(',
    '  <StrictMode>',
    '    <App />',
    '  </StrictMode>,',
    ');',
    '',
    '/* AIDEVENGINE_PREVIEW_READINESS_HANDSHAKE_V1 */',
    'requestAnimationFrame(() => {',
    '  requestAnimationFrame(() => {',
    "    try {",
    "      document.documentElement.setAttribute('data-aidev-preview-hydrated', '1');",
    "      document.documentElement.setAttribute('data-aidev-preview-route-ready', '1');",
    "      document.documentElement.setAttribute('data-aidev-preview-ready', '1');",
    "      const mount = document.getElementById('root');",
    "      if (mount) mount.setAttribute('data-aidev-preview-ready', '1');",
    '    } catch {',
    '      /* ignore */',
    '    }',
    '  });',
    '});',
    '',
  ].join('\n');
}

function minimalAppEntry(primary: WorkspaceFeatureModuleEvidence, useRouter: boolean): string {
  if (useRouter) {
    return [
      "import FeatureAppRouter from './features/FeatureAppRouter';",
      '',
      'export default function App() {',
      '  return (',
      `    <main data-direct-feature-app="true" data-root-feature="${primary.id}">`,
      '      <FeatureAppRouter />',
      '    </main>',
      '  );',
      '}',
      '',
    ].join('\n');
  }
  const componentName = pascalCase(primary.id) + 'Feature';
  const importFrom = `./features/${primary.id}`;
  return [
    `import ${componentName} from '${importFrom}';`,
    '',
    'export default function App() {',
    '  return (',
    `    <main data-direct-feature-app="true" data-root-feature="${primary.id}">`,
    `      <${componentName} />`,
    '    </main>',
    '  );',
    '}',
    '',
  ].join('\n');
}

function minimalFeatureAppRouter(modules: WorkspaceFeatureModuleEvidence[]): string {
  const first = modules[0]?.id ?? '';
  const navButtons = modules
    .map(
      (m) =>
        `        <button type="button" className={\`modular-nav-item \${activeModuleId === '${m.id}' ? 'is-active' : ''}\`} onClick={() => setActiveModuleId('${m.id}')}>${m.name}</button>`,
    )
    .join('\n');
  return [
    "import { useMemo, useState } from 'react';",
    "import { FEATURE_REGISTRY } from './registry';",
    '',
    '/** Generic modular feature router — regenerated by the workspace materialization stabilizer. */',
    'export default function FeatureAppRouter() {',
    `  const [activeModuleId, setActiveModuleId] = useState('${first}');`,
    '  const activeEntry = useMemo(',
    "    () => FEATURE_REGISTRY.find((entry) => entry.id === activeModuleId) ?? FEATURE_REGISTRY[0],",
    '    [activeModuleId],',
    '  );',
    '  const ActiveComponent = activeEntry?.component;',
    '',
    '  return (',
    '    <div className="feature-app-router" data-modular-feature-router="v1">',
    '      <nav className="modular-nav" aria-label="Feature modules">',
    navButtons,
    '      </nav>',
    '      <div className="modular-active-feature">',
    '        {ActiveComponent ? <ActiveComponent /> : null}',
    '      </div>',
    '    </div>',
    '  );',
    '}',
    '',
  ].join('\n');
}

function minimalFeatureComponent(mod: WorkspaceFeatureModuleEvidence): string {
  const componentName = pascalCase(mod.id) + 'Feature';
  return [
    'export default function ' + componentName + '() {',
    '  return (',
    `    <section className="modular-feature" data-feature-module="${mod.id}" data-modular-feature-v1="true">`,
    '      <header className="modular-feature-header">',
    `        <h2>${mod.name}</h2>`,
    '      </header>',
    '      <div className="modular-feature-body">',
    `        <p>${mod.name} is ready.</p>`,
    '      </div>',
    '    </section>',
    '  );',
    '}',
    '',
  ].join('\n');
}

function minimalFeatureTypes(mod: WorkspaceFeatureModuleEvidence): string {
  const recordName = pascalCase(mod.id) + 'Record';
  return `export interface ${recordName} {\n  id: string;\n  label: string;\n}\n`;
}

function minimalFeatureService(mod: WorkspaceFeatureModuleEvidence): string {
  const recordName = pascalCase(mod.id) + 'Record';
  const fnName = 'list' + pascalCase(mod.id) + 'Records';
  return [
    `import type { ${recordName} } from './${mod.id}.types';`,
    '',
    `export function ${fnName}(): ${recordName}[] {`,
    '  return [];',
    '}',
    '',
  ].join('\n');
}

function minimalFeatureValidation(mod: WorkspaceFeatureModuleEvidence): string {
  const constName = mod.id.toUpperCase().replace(/-/g, '_') + '_VALIDATION';
  return `export const ${constName} = {\n  rules: [] as string[],\n};\n`;
}

function minimalFeatureBarrel(mod: WorkspaceFeatureModuleEvidence): string {
  const componentName = pascalCase(mod.id) + 'Feature';
  return `export { default } from './${componentName}';\n`;
}

function minimalRegistryTs(modules: WorkspaceFeatureModuleEvidence[]): string {
  const imports = modules
    .map((m) => `import ${pascalCase(m.id)}Feature from './${m.id}';`)
    .join('\n');
  const entries = modules
    .map(
      (m) =>
        `  { id: '${m.id}', name: '${m.name}', route: '${m.route}', component: ${pascalCase(m.id)}Feature, sourcePath: '${m.componentPath.replace(/\\/g, '/')}', contractId: 'feature-${m.id}', promptTerms: [] as string[], status: 'generated' as const },`,
    )
    .join('\n');
  return [imports, '', 'export const FEATURE_REGISTRY = [', entries, '] as const;', ''].join('\n');
}

function minimalRoutesTs(): string {
  return [
    "import { FEATURE_REGISTRY } from './registry';",
    '',
    'export const APP_ROUTES = FEATURE_REGISTRY.map((entry) => ({',
    '  path: entry.route,',
    '  moduleId: entry.id,',
    '  name: entry.name,',
    '  component: entry.component,',
    '  sourcePath: entry.sourcePath,',
    '})) as const;',
    '',
  ].join('\n');
}

function minimalManifestPatch(workspaceDir: string, modules: WorkspaceFeatureModuleEvidence[]): string {
  return `${JSON.stringify(
    {
      contractVersion: 'WORKSPACE_MATERIALIZATION_STABILIZER_V1_RECONSTRUCTED',
      generatedFeatureModulesCount: modules.length,
      featureModules: modules.map((m) => m.id),
      routes: modules.map((m) => m.route),
      featureModuleDetails: modules,
    },
    null,
    2,
  )}\n`;
}

export interface ApplyRepairsResult {
  repairActions: WorkspaceMaterializationRepairAction[];
  remainingFindings: WorkspaceMaterializationFinding[];
}

export function applyRepairs(
  workspaceDir: string,
  findings: WorkspaceMaterializationFinding[],
  featureModules: WorkspaceFeatureModuleEvidence[],
  manifest: Record<string, unknown> | null,
): ApplyRepairsResult {
  const repairActions: WorkspaceMaterializationRepairAction[] = [];
  const remainingFindings: WorkspaceMaterializationFinding[] = [];

  const record = (
    finding: WorkspaceMaterializationFinding,
    applied: boolean,
    description: string,
    detail: string,
  ): void => {
    repairActions.push({
      readOnly: true,
      findingId: finding.id,
      kind: finding.kind,
      path: finding.path,
      description,
      applied,
      detail,
    });
    if (!applied) remainingFindings.push(finding);
  };

  const usesRouter = fileExists(workspaceDir, 'src/features/FeatureAppRouter.tsx') || featureModules.length > 1;

  for (const finding of findings) {
    if (finding.severity === 'BLOCKING') {
      remainingFindings.push(finding);
      continue;
    }

    try {
      switch (finding.kind) {
        case 'MISSING_TSCONFIG':
          writeGenerated(workspaceDir, 'tsconfig.json', minimalTsconfig());
          record(finding, true, 'Regenerated tsconfig.json', 'Wrote a minimal, standard tsconfig.json.');
          break;

        case 'MISSING_VITE_CONFIG':
          writeGenerated(workspaceDir, 'vite.config.ts', minimalViteConfig());
          record(finding, true, 'Regenerated vite.config.ts', 'Wrote a minimal, standard Vite config.');
          break;

        case 'MISSING_INDEX_HTML':
          writeGenerated(workspaceDir, 'index.html', minimalIndexHtml());
          record(finding, true, 'Regenerated index.html', 'Wrote a minimal index.html mounting src/main.tsx.');
          break;

        case 'MISSING_ROOT_ENTRY':
          writeGenerated(workspaceDir, 'src/main.tsx', minimalMainEntry());
          record(finding, true, 'Regenerated src/main.tsx', 'Wrote a minimal root entry point.');
          break;

        case 'MISSING_APP_ENTRY': {
          const primary = featureModules[0];
          if (!primary) {
            record(finding, false, 'Could not regenerate App entry', 'No feature module evidence available.');
            break;
          }
          writeGenerated(workspaceDir, 'src/App.tsx', minimalAppEntry(primary, usesRouter));
          record(
            finding,
            true,
            'Regenerated src/App.tsx',
            `Mounted the ${usesRouter ? 'feature router' : `"${primary.id}"`} entry from workspace evidence.`,
          );
          break;
        }

        case 'MISSING_FEATURE_ROUTER':
          writeGenerated(workspaceDir, 'src/features/FeatureAppRouter.tsx', minimalFeatureAppRouter(featureModules));
          record(finding, true, 'Regenerated FeatureAppRouter.tsx', 'Wrote a minimal generic router over the feature registry.');
          break;

        case 'MISSING_REQUIRED_FILE': {
          const mod = featureModules.find(
            (m) => m.componentPath === finding.path || m.servicePath === finding.path || m.typesPath === finding.path || m.validationPath === finding.path,
          );
          if (!mod || !finding.path) {
            record(finding, false, 'Could not regenerate file', 'No matching feature module evidence.');
            break;
          }
          let content: string | null = null;
          if (finding.path === mod.componentPath) content = minimalFeatureComponent(mod);
          else if (finding.path === mod.typesPath) content = minimalFeatureTypes(mod);
          else if (finding.path === mod.servicePath) content = minimalFeatureService(mod);
          else if (finding.path === mod.validationPath) content = minimalFeatureValidation(mod);
          if (!content) {
            record(finding, false, 'Could not regenerate file', 'Unrecognized generated file kind.');
            break;
          }
          writeGenerated(workspaceDir, finding.path, content);
          record(finding, true, `Regenerated ${finding.path}`, `Wrote a minimal replacement for feature module "${mod.id}".`);
          break;
        }

        case 'MISSING_BARREL_EXPORT': {
          const mod = featureModules.find((m) => finding.path === `src/features/${m.id}/index.ts`);
          if (!mod || !finding.path) {
            record(finding, false, 'Could not regenerate barrel export', 'No matching feature module evidence.');
            break;
          }
          writeGenerated(workspaceDir, finding.path, minimalFeatureBarrel(mod));
          record(finding, true, `Regenerated ${finding.path}`, `Wrote a minimal barrel export for feature module "${mod.id}".`);
          break;
        }

        case 'MISSING_ROUTE_REGISTRATION':
          if (featureModules.length === 0) {
            record(finding, false, 'Could not repair route registration', 'No feature module evidence available.');
            break;
          }
          writeGenerated(workspaceDir, 'src/features/registry.ts', minimalRegistryTs(featureModules));
          writeGenerated(workspaceDir, 'src/features/routes.ts', minimalRoutesTs());
          record(
            finding,
            true,
            'Repaired feature registry and routes',
            `Regenerated registry.ts and routes.ts from ${featureModules.length} known feature module(s).`,
          );
          break;

        case 'BROKEN_IMPORT': {
          // Broken imports for known generated files are already covered by the more specific
          // MISSING_REQUIRED_FILE / MISSING_BARREL_EXPORT / MISSING_FEATURE_ROUTER repairs above,
          // which regenerate the missing target. Here we handle the remaining generic case: the
          // target file actually exists nearby but the import path itself is wrong (e.g. a typo) —
          // repaired by rewriting only that one import statement, never unrelated file content.
          if (!finding.path) {
            record(finding, false, 'Left unrepaired', 'No file path associated with this finding.');
            break;
          }
          const stillBroken = findBrokenImportsInFile(workspaceDir, finding.path);
          if (stillBroken.length === 0) {
            record(finding, true, 'Import already resolved', 'A prior repair in this pass already fixed the referenced file.');
            break;
          }
          const absFile = join(workspaceDir, finding.path);
          let content = readFileSync(absFile, 'utf8');
          let allFixed = true;
          for (const broken of stillBroken) {
            const fuzzyTarget = findFuzzyImportMatch(workspaceDir, finding.path, broken.importPath);
            if (fuzzyTarget) {
              content = content.split(`'${broken.importPath}'`).join(`'${fuzzyTarget}'`).split(`"${broken.importPath}"`).join(`"${fuzzyTarget}"`);
            } else {
              allFixed = false;
            }
          }
          if (content !== readFileSync(absFile, 'utf8')) {
            writeFileSync(absFile, content, 'utf8');
          }
          if (allFixed) {
            record(finding, true, `Repaired import path in ${finding.path}`, 'Rewrote the broken import to point at the matching file found on disk.');
          } else {
            record(finding, false, 'Left unrepaired', 'The broken import does not match any file this stabilizer can safely resolve.');
          }
          break;
        }

        case 'MISSING_MANIFEST':
          writeGenerated(workspaceDir, '.generated-app-manifest.json', minimalManifestPatch(workspaceDir, featureModules));
          record(finding, true, 'Reconstructed materialization manifest', 'Wrote a minimal manifest reflecting the feature modules found on disk.');
          break;

        case 'MANIFEST_INCONSISTENCY': {
          if (!manifest) {
            record(finding, false, 'Could not repair manifest', 'Manifest is unavailable.');
            break;
          }
          const patched = {
            ...manifest,
            generatedFeatureModulesCount: featureModules.length,
            featureModules: featureModules.map((m) => m.id),
            routes: featureModules.map((m) => m.route),
          };
          writeGenerated(workspaceDir, '.generated-app-manifest.json', `${JSON.stringify(patched, null, 2)}\n`);
          record(finding, true, 'Patched manifest consistency', 'Corrected manifest counts and route list to match workspace evidence.');
          break;
        }

        case 'MISSING_ASSET':
          record(finding, false, 'Left unrepaired', 'Binary/generated assets cannot be safely fabricated.');
          break;

        default:
          record(finding, false, 'Left unrepaired', 'No repair strategy is defined for this finding kind.');
      }
    } catch (err) {
      record(finding, false, 'Repair attempt failed', err instanceof Error ? err.message : String(err));
    }
  }

  return { repairActions, remainingFindings };
}

